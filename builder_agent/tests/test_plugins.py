# 6. E2E / Integration tests for generate/verify hooks in orchestrate.py


@patch("builder_agent.generate.ask")
@patch("builder_agent.verify.ask")
@patch("builder_agent.verify.run_code")
def test_subtask_hooks_composition_in_verify_and_generate(
    mock_run_code, mock_verify_ask, mock_ask
):
    # Setup mocks
    mock_ask.return_value = "def hello():\n    return 'world'"
    mock_verify_ask.return_value = '{"score": 9, "issues": []}'
    mock_run_code.return_value = (True, "all tests passed")

    # Prepare plugin manager with Custom Generator
    manager = PluginManager()
    manager.generators.append(MockGeneratorPlugin())
    manager.post_processors.append(MockPostProcessorPlugin())
    manager.verifiers.append(MockVerifierPlugin())

    subtask = SubTask(id="custom", description="desc", acceptance_criteria=[])
    spec = Spec(
        request="req",
        description="desc",
        acceptance_criteria=[],
        assumptions=[],
        output_type="python_module",
    )

    # 1. Test generate integration
    from builder_agent.generate import generate

    code = generate(subtask, spec, plugin_manager=manager)
    # Bypassed default generate block entirely and returned custom code
    assert code == "generated by plugin"

    # 2. Test verify integration with blocking failure
    from builder_agent.verify import verify

    bad_subtask = SubTask(
        id="fail_blocking", description="desc", acceptance_criteria=[]
    )
    with patch.object(config, "PLUGINS_DISABLED", ["LinterPlugin"]):
        verdict = verify(
            bad_subtask,
            "code",
            output_type="python_module",
            plugin_manager=manager,
        )
    # Merged issues and forced passed=False
    assert not verdict.passed
    assert "linter error" in verdict.issues
