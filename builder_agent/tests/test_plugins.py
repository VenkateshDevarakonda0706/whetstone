def test_generator_plugin_bypass_and_fallback():
    manager = PluginManager()
    manager.generators.append(MockGeneratorPlugin())

    gen_ctx = GenerationContext(
        subtask_id="custom",
        subtask_description="Custom generation",
        acceptance_criteria=[],
        spec_request="",
        spec_description="",
    )
    context = PluginContext(workspace_dir=".", output_type="python")

    # If plugin returns code, use it
    code = manager.run_generators(gen_ctx, context)
    assert code == "generated by plugin"

    # If plugin returns None, fallback to default (manager returns None)
    fallback_ctx = GenerationContext(
        subtask_id="normal",
        subtask_description="Normal generation",
        acceptance_criteria=[],
        spec_request="",
        spec_description="",
    )
    code2 = manager.run_generators(fallback_ctx, context)
    assert code2 is None


