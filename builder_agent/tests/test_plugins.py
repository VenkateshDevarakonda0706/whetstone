def test_decorator_registration():
    from builder_agent.plugin_system import (
        register_generator,
        register_post_processor,
        register_verifier,
        registered_plugins,
    )

    registered_plugins.clear()

    @register_generator
    class DecoratedGen:
        def generate(self, gen_context, context, on_chunk=None):
            return "ok"

    @register_verifier
    class DecoratedVerify:
        def verify(self, subtask, code, context):
            return None

    @register_post_processor
    class DecoratedPost:
        def post_process_subtask(self, subtask, code, context):
            return code

    manager = PluginManager()
    manager.discover_and_register()

    assert any(isinstance(g, DecoratedGen) for g in manager.generators)
    assert any(isinstance(v, DecoratedVerify) for v in manager.verifiers)
    assert any(isinstance(p, DecoratedPost) for p in manager.post_processors)


