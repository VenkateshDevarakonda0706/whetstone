# Mock Plugin classes
class MockGeneratorPlugin:
    def generate(
        self, gen_context: GenerationContext, context: PluginContext, on_chunk=None
    ) -> str | None:
        if gen_context.feedback == "fail":
            raise ValueError("Test generation error")
        if gen_context.subtask_id == "custom":
            return "generated by plugin"
        return None


class MockPostProcessorPlugin:
    def post_process_subtask(
        self, subtask: SubTask, code: str, context: PluginContext
    ) -> str:
        if subtask.id == "error_subtask":
            raise ValueError("Post processor error")
        return code + "\n# processed subtask"

    def post_process_artifact(
        self, spec: Spec, code: str | dict[str, str], context: PluginContext
    ) -> str | dict[str, str]:
        if spec.description == "error_artifact":
            raise ValueError("Artifact post processor error")
        if isinstance(code, str):
            return code + "\n# processed artifact"
        return code


class MockVerifierPlugin:
    def verify(
        self, subtask: SubTask, code: str | dict[str, str], context: PluginContext
    ) -> PluginVerificationResult | None:
        if subtask.id == "crash_verifier":
            raise ValueError("Verifier plugin error")
        if subtask.id == "fail_blocking":
            return PluginVerificationResult(
                passed=False, issues=["linter error"], blocking=True
            )
        if subtask.id == "fail_non_blocking":
            return PluginVerificationResult(
                passed=False, issues=["style warning"], blocking=False
            )
        return PluginVerificationResult(passed=True, issues=[])


