def test_local_plugins_discovery_and_failure_isolation():
    # Setup temporary directory for local plugins
    with tempfile.TemporaryDirectory() as tmpdir:
        # Create a valid local plugin py file
        valid_plugin = """
class LocalGenPlugin:
    def generate(self, gen_context, context, on_chunk=None):
        return "local code"
"""
        # Create an invalid local plugin py file (contains syntax error)
        invalid_plugin = """
class InvalidPlugin:
    def generate(self, gen_context, context, on_chunk=None)
        return "oops"
"""
        pathlib.Path(tmpdir).mkdir(parents=True, exist_ok=True)
        with open(os.path.join(tmpdir, "valid.py"), "w", encoding="utf-8") as f:
            f.write(valid_plugin)
        with open(os.path.join(tmpdir, "invalid.py"), "w", encoding="utf-8") as f:
            f.write(invalid_plugin)

        manager = PluginManager(plugin_dir=tmpdir)
        with patch.object(config, "PLUGIN_DIR", tmpdir), patch.object(
            config, "PLUGINS_DISABLED", []
        ):
            manager.discover_and_register()

            # Should discover LocalGenPlugin and bypass/log the invalid syntax file
            assert any(
                g.__class__.__name__ == "LocalGenPlugin" for g in manager.generators
            )


